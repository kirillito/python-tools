import math;

def merge(arr1, arr2):
    res = [];
    i = 0;
    j = 0;
    
    while (i < len(arr1) and j < len(arr2)):
        if arr1[i] < arr2[j]:
            res.append(arr1[i]);
            i += 1;
        else:
            res.append(arr2[j]);
            j += 1;
    
    while i < len(arr1):
        res.append(arr1[i]);
        i += 1;
    while j < len(arr2):
        res.append(arr2[j]);
        j += 1;
        
    return res;
    
    
def mergeSort(arr):
    if len(arr) <= 1:
        return arr;
        
    mid = math.floor(len(arr) / 2);
    left = mergeSort(arr[:mid])
    right = mergeSort(arr[mid:])
    
    return merge(left, right);
            
